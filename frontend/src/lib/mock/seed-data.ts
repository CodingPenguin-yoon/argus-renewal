const baseDate = new Date("2026-03-04T09:00:00+09:00");

function hoursAgo(hours: number) {
  return new Date(baseDate.getTime() - hours * 60 * 60 * 1000).toISOString();
}

function daysLater(days: number, hour: number) {
  const date = new Date(baseDate.getTime() + days * 24 * 60 * 60 * 1000);
  date.setHours(hour, 0, 0, 0);
  return date.toISOString();
}

